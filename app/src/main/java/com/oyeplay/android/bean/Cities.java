public class Cities     {


    private String cn;

    private String la;

    private String lo;

    public Cities(){
    }

    /**
     *
     * @return
     * The cn
     */
    public String getCn() {
        return cn;
    }

    /**
     *
     * @param cn
     * The cn
     */
    public void setCn(String cn) {
        this.cn = cn;
    }

    /**
     *
     * @return
     * The la
     */
    public String getLa() {
        return la;
    }

    /**
     *
     * @param la
     * The la
     */
    public void setLa(String la) {
        this.la = la;
    }

    /**
     *
     * @return
     * The lo
     */
    public String getLo() {
        return lo;
    }

    /**
     *
     * @param lo
     * The lo
     */
    public void setLo(String lo) {
        this.lo = lo;
    }

}
